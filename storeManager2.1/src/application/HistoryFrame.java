    private void findButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_findButtonActionPerformed
        // TODO add your handling code here:
        String text = idText.getText();
        boolean isFound = false;
        for(Bill b : Manager.bills) {
            isFound = true;
            if (b.getIdBill().equals(text)) {
                DefaultTableModel model = (DefaultTableModel) table.getModel();
                model.setRowCount(0);
                model.addRow(new Object[] 
                {b.getIdBill(), b.getDate(), b.getTime(), b.getType(), b.getSum(), b.getEmployee()});
            }
        }
        if (!isFound) {
            JOptionPane.showInternalMessageDialog(null, "Không tìm thấy hóa đơn!");
        }
        
    }//GEN-LAST:event_findButtonActionPerformed

    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        // TODO add your handling code here:
        String select = (String) table.getValueAt(table.getSelectedRow(), table.getSelectedColumn());
        
        Bill bill = null;
        for(Bill b : Manager.bills) {
            if(b.getIdBill().equals(select)) {
                bill = b;
                
            }
        }
        
        PrintBill pb = new PrintBill(bill);
        pb.show(true);
        
    }//GEN-LAST:event_tableMouseClicked


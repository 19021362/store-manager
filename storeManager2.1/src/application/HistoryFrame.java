    private void loadTable() {
        try {
            DefaultTableModel model = (DefaultTableModel) table.getModel();
            Manager.products = Load.loadProduct();
            Manager.allBill_details = Load.loadBillDetail(Manager.products);
            Manager.bills = Load.loadBills(Manager.allBill_details);
            model.setRowCount(0);
            
            for(Bill b : Manager.bills) {
                model.addRow(new Object[] 
                {b.getIdBill(), b.getDate(), b.getTime(), b.getType(), b.getSumC(), b.getEmployee()});
            }
        } catch (IOException ex) {
            Logger.getLogger(HistoryFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

